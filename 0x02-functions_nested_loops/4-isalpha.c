#include "main.h"
/**
 * _isalpha -A function that checks for alphabetic character
 * betty style doc for function main is inserted here
*@c:the char that is being checked
 *Return: 1 if c is a letter, lowercase or uppercase otherwise return 0..
 */
int _isalpha(int c)
{
if (c >= 'a' && c <= 'z')
{
return (1);
}
else
{
return (0);
}
return (0);
}
